void main() {
  // Equality & Relational Operator

  // EQUAL SYMBOL IS  -->   ==
  num num1 = 34;
  num num2 = 34;
  print(num1 == num2); //true

  // NOT EQUAL TO -->    !=
  num num3 = 34;
  num num4 = 4;
  print(num3 != num4); //true

  // Grater than -->  >

  num num5 = 34;
  num num6 = 4;
  print(num3 > num4); //true

  // Lass than -->  <

  num num7 = 34;
  num num8 = 4;
  print(num7 < num8); //true

// Greater than or equal to
  num num9 = 34;
  num num10 = 4;
  print(num9 >= num10);

  // Less than or equal to
  num num11 = 34;
  num num12 = 4;
  print(num11 <= num12);
}
